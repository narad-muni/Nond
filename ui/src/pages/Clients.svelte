<script>
    //Imports

    import {
        Button,
        Modal,
        Table,
        TableBody,
        TableBodyCell,
        TableBodyRow,
        Checkbox,
        A,
        Label,
        Helper,
        Input,
        Toggle,
        Alert,
        Textarea,
        Select
    } from "flowbite-svelte";

    import { DataHandler, ThFilter } from "@vincjo/datatables";
    import Th from "../component/Th.svelte";
    import ThSearch from "../component/ThSearch.svelte";
    import DataTable from "../component/DataTable.svelte";
    import utils from '../utils';

    // Intialization

    let createModal, createTasksModal, actionsModals, deleteModal, uploadModal, allColumns = false;
    let selectedRows = new Set();

    let headers, data, createdObject={}, actionsIndex, actionsObject, templateFile;
    let handler, rows;
    let automaticAssign = [
        {name:"Divide",value:"Divide"},
        {name:"By Client",value:"By Client"}
    ]
    let error="", success="", assignedUser, autoAssignType, users;

    // fetch data

    (async ()=>{
        headers = await utils.get('/api/master_template/options/clients');
        users = await utils.get('/api/employee/options');
        data = await utils.get('/api/client/master/');
        
        handler = new DataHandler(
            data,
            {
                rowsPerPage:50
            }
        )

        rows = handler.getRows();

        $rows.forEach((v) => {
            v["_selected"] = 0;
        });
    })()

    //Reactive variables

    $: checked = utils.compareSets(selectedRows,new Set(($rows||[]).map(i => i.id))); // $rows||[] is used to wait and not fail
    $: indeterminate = selectedRows.size > 0 && !checked;
    $: buttonDisabled = selectedRows.size == 0;

    //Functions

    function addSelection(e){
        
        let id = e.target.getAttribute('oid');// get object id, which is primary key in db

        if(id){ // if oid is passed, then element is column
            if(e.target.checked){
                selectedRows.add(parseInt(id));
            }else{
                selectedRows.delete(parseInt(id));
            }

        }else{//element is header, global checkbox
            if(e.target.checked){

                $rows.forEach((r) => {
                    r._selected = true;
                    selectedRows.add(r.id);
                });

            }else{

                $rows.forEach((r) => {
                    r._selected = false;
                    selectedRows.delete(r.id);
                });

            }
        }

        selectedRows = selectedRows;
        handler.setRows($rows);
    }

    async function openActionsModal(e){
        let oid = e.target.innerText;
        $rows.every((el,i) => {
            if(el.id == oid){
                actionsIndex = i;
                return false;
            }
            return true;
        });

        actionsObject = await utils.get('/api/client/read_by_id/'+$rows[actionsIndex].id);

        actionsModals = true;
    }

    async function updateData(){
        await utils.put_form('/api/client/update',utils.getFormData(actionsObject));
    }

    async function loadTemplateData(){
        if(templateFile){
            let resp = await utils.post_form('/api/client/load_template_data',utils.getFormData({"template":templateFile}));
            if(resp.status != "success"){
                error=resp.message;
            }else{
                success=resp.message;
                uploadModal = false;
            }
        }else{
            error="Please select a file";
        }
    }

    async function reloadData(){
        if(allColumns){
            data = await utils.get('/api/client/read_full/');
        }else{
            data = await utils.get('/api/client/read_master/');
        }

        selectedRows.clear();
        selectedRows = selectedRows;
        handler.setRows(data);
    }

    async function viewAllColumns(){
        allColumns = true;
        data = await utils.get('/api/client/read_full/');

        data.forEach((v) => {
            if(selectedRows.has(v["id"])){
                v["_selected"] = true;
            }else{
                v["_selected"] = false;
            }
        });

        handler.setRows(data);
    }
    
    async function download(){
        let downloadData;

        //check if all columns are fetched to avoid api call
        if(allColumns){
            downloadData = data;
        }else{
            downloadData = await utils.get('/api/client/read_full');
        }

        //download selected rows if selected, else if everything or nothing is selected, download full
        if(indeterminate){
            downloadData = downloadData.filter((i) => {
                return selectedRows.has(i.id);
            });
        }

        utils.downloadCSVFile(Object.keys(headers.columns),downloadData,'ClientMaster');
    }

    async function deleteSelected(){
        for (let i = 0; i < $rows.length; i++) {
            if (selectedRows.has($rows[i].id)) {
                $rows.splice(i, 1);
                i--;
            }
        }
        selectedRows.clear();
        handler.setRows($rows);
        selectedRows = selectedRows;
    }

    async function createData(){

    }

</script>

<main class="flex flex-col w-full min-w-0 max-h-full p-2">
